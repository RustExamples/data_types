fn main() {
    // Arrays store fixed elements of same type and can't grow or shrink
    let array = [1, 2, 3, 4, 5];

    let one = array[0];
    println!("one is {}", one);

    let months =  ["January", "February", "March", "April", "May", "June", "July",
              "August", "September", "October", "November", "December"];

    let jan = months[0];
    println!("jan is {}", jan);
}
