fn main() {
    // addition
    let sum = 5 + 10;
    println!("sum is {}", sum);

    // subtraction
    let difference = 10 - 5;
    println!("difference is {}", difference);

    // multiplication
    let product = 10 * 5;
    println!("product is {}", product);

    // division
    let quotient = 10/5;
    println!("quotient is {}", quotient);

    // remainder
    let remainder = 10%5;
    println!("remainder is {}", remainder);
}
